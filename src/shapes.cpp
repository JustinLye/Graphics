#include"..\include\shapes.h"

jlg::Shape::Shape(GLfloat VertexDim, GLfloat VertexCount) :
	vertexDim(VertexDim),
	vertexCount(VertexCount),
	elementCount(VertexDim * VertexCount),
	vertexData(nullptr) {}

jlg::Shape::~Shape() {
	if(vertexData != nullptr)
		delete[] vertexData;
}

jlg::Cube::Cube() : Shape(5.0f, 36.0f) {
	AllocateVertexData();
}

